#include "ft_rush.h"

void	ft_row(int x, char *chars, char **str, int *j)
{
	int i;

	i = 0;
	while (i < x)
	{
		if (i == 0)
			(*str)[*j] = chars[0];
		else if (i == x - 1)
			(*str)[*j] = chars[2];
		else
			(*str)[*j] = chars[1];
		i++;
		(*j)++;
	}
	(*str)[*j] = '\n';
	(*j)++;
}
