void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long tmp;

	tmp = nb;
	if (tmp < 0)
	{
		ft_putchar('-');
		tmp *= -1;
	}
	if (tmp >= 10)
	{
		ft_putnbr(tmp / 10);
	}
	ft_putchar(tmp % 10 + '0');
}
