int		ft_iterative_factorial(int nb)
{
	int out;

	if (nb < 0 || nb > 12)
	{
		out = 0;
	}
	else
	{
		out = 1;
	}
	while (nb > 1)
	{
		out *= nb;
		nb--;
	}
	return (out);
}
